void hash_string_to_hash(const uint8_t* string, uint8_t* output) {
    uint16_t i = 0;
    while (string[i] != 0x00) {
        output[i / 2] = (hextable[string[i]] << 4) | (hextable[string[i + 1]]);
        i += 2;
    }
}
